"""Risk-evolution evaluation (Phase 4).

Measures risk evolution classification accuracy against a reusable gold dataset.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

from app.risk.evolution.evolution_service import RiskEvolutionService

_DEFAULT_PATH = Path(__file__).with_name("gold_dataset.json")


@dataclass
class MockRiskFactor:
    id: Any
    risk_name: str
    normalized_risk_name: str
    category: str
    severity: str
    confidence_score: float = 0.9


@dataclass
class GoldEvolutionExample:
    id: str
    company_id: str
    prior: list[MockRiskFactor]
    current: list[MockRiskFactor]
    expected: list[dict]


@dataclass
class RiskEvolutionEvaluationReport:
    num_examples: int
    total_expected: int
    total_generated: int
    correct: int
    accuracy: float
    precision: float
    recall: float
    per_evolution_type: dict[str, dict] = field(default_factory=dict)

    def as_dict(self) -> dict:
        from dataclasses import asdict
        return asdict(self)


def load_gold_dataset(path: str | Path | None = None) -> list[GoldEvolutionExample]:
    p = Path(path) if path else _DEFAULT_PATH
    raw = json.loads(p.read_text(encoding="utf-8"))
    
    examples = []
    for e in raw.get("examples", []):
        prior_list = [
            MockRiskFactor(
                id=r["id"],
                risk_name=r["risk_name"],
                normalized_risk_name=r.get("normalized_risk_name", r["risk_name"].upper()),
                category=r["category"],
                severity=r["severity"],
            )
            for r in e.get("prior", [])
        ]
        curr_list = [
            MockRiskFactor(
                id=r["id"],
                risk_name=r["risk_name"],
                normalized_risk_name=r.get("normalized_risk_name", r["risk_name"].upper()),
                category=r["category"],
                severity=r["severity"],
            )
            for r in e.get("current", [])
        ]
        examples.append(
            GoldEvolutionExample(
                id=e["id"],
                company_id=e["company_id"],
                prior=prior_list,
                current=curr_list,
                expected=e.get("expected", []),
            )
        )
    return examples


class RiskEvolutionEvaluator:
    def __init__(self, service: RiskEvolutionService | None = None) -> None:
        self.service = service or RiskEvolutionService()

    def evaluate(self, examples: list[GoldEvolutionExample]) -> RiskEvolutionEvaluationReport:
        total_expected = 0
        total_generated = 0
        correct = 0
        per_evolution_type: dict[str, dict] = {}

        for ex in examples:
            # Cast list[MockRiskFactor] to expected list[RiskFactor] type for type checking
            result = self.service.build_evolution(
                ex.company_id,
                ex.current,  # type: ignore
                ex.prior,    # type: ignore
            )

            total_generated += len(result.rows)

            # Map generated by (previous_risk_id, current_risk_id)
            generated_map = {}
            for r in result.rows:
                prev_id = str(r.previous_risk_id) if r.previous_risk_id is not None else None
                curr_id = str(r.current_risk_id) if r.current_risk_id is not None else None
                generated_map[(prev_id, curr_id)] = r.evolution_type

            for exp in ex.expected:
                total_expected += 1
                etype = exp["evolution_type"]
                stats = per_evolution_type.setdefault(etype, {"expected": 0, "correct": 0})
                stats["expected"] += 1

                exp_prev = exp["previous_risk_id"]
                exp_curr = exp["current_risk_id"]

                got_type = generated_map.get((exp_prev, exp_curr))
                if got_type == etype:
                    correct += 1
                    stats["correct"] += 1

        return RiskEvolutionEvaluationReport(
            num_examples=len(examples),
            total_expected=total_expected,
            total_generated=total_generated,
            correct=correct,
            accuracy=round(correct / total_expected, 4) if total_expected else 0.0,
            precision=round(correct / total_generated, 4) if total_generated else 0.0,
            recall=round(correct / total_expected, 4) if total_expected else 0.0,
            per_evolution_type=per_evolution_type,
        )
